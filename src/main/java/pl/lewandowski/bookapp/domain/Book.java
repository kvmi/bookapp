package pl.lewandowski.bookapp.domain;

public class Book {
    String title;
    String ISBN;
    Person author;

    public Book(String title, String ISBN, Person author) {
        this.title = title;
        this.ISBN = ISBN;
        this.author = author;
    }

    public Book() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }

    public Person getAuthor() {
        return author;
    }

    public void setAuthor(Person author) {
        this.author = author;
    }

    @Override
    public String toString() {
        return "Book{" +
                "title='" + title + '\'' +
                ", ISBN='" + ISBN + '\'' +
                ", author=" + author +
                '}';
    }
}

